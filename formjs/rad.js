var rad_nu = $(".random_button");
for(var i = 0; i < rad_nu.length; i++){
    var target = Math.floor(Math.random() * rad_nu.length -1) + 1;
    var target2 = Math.floor(Math.random() * rad_nu.length -1) +1;
    rad_nu.eq(target).before(rad_nu.eq(target2));
}

